
class directory():
  
  #==============================================================================#
  def __init__(self) -> None:
    pass
  
  #==============================================================================#
  
  #==============================================================================#
  def parse(self, folder_name: str) -> str:
    for _forbidden in ['/','<', '>', ':', '"', '\\', '|', '?', '*']:
      folder_name = folder_name.replace(_forbidden, '')
    return folder_name
  #==============================================================================#